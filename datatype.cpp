#include<iostream>
using namespace std;
int main()
{
    //int datatype
    int apples = 5 ;
    cout<<apples<<endl;
    cout<<"size of int: "<<sizeof(apples)<<endl;

    //char datatype
    char s ='A';
    cout<<s<<endl;
    cout<<"size of s: "<<sizeof(s)<<endl;

    //Bool datatype
    bool f =true;
    cout<<f<<endl;
    cout<<"size of bool:"<<sizeof(f)<<endl;

    //float datatype
    float pi = 1.5;
    cout<<pi<<endl;
    cout<<"size of float: "<<sizeof(pi)<<endl;

}